def _apply_channel_weights(
    hypotheses: list[Hypothesis], calibration: CalibrationConfig | None = None
):
    """Apply validation-learned evidence-channel weights in log space."""
    if not hypotheses or calibration is None or not calibration.channel_weights:
        return
    aliases = {
        "rate": (
            "rate",
            "stellar_motion",
            "extreme_rate",
            "non_satellite_rate_penalty",
            "moving_source_penalty",
            "zero_motion",
        ),
        "magnitude": ("magnitude",),
        "morphology": (
            "morphology",
            "cosmic_ray_morphology",
            "streak_morphology",
            "blend_morphology",
            "edge_morphology",
            "non_cosmic_morphology_penalty",
            "non_streak_morphology_penalty",
            "non_blend_morphology_penalty",
            "host_morphology",
        ),
        "orbit_fit": (
            "orbit_state",
            "rms_fit",
            "orbital_elements_context",
            "orbital_elements_context_error",
        ),
        "xmatch": ("skybot_clean_rare_bonus",),
        "artifact_context": (
            "single_image_penalty",
            "multinight_motion_penalty",
            "coherent_arc_penalty",
            "single_detection_bonus",
            "single_detection",
            "high_rms",
            "generic_artefact_fit",
            "stationary_context_penalty",
        ),
        "color": ("color",),
    }
    for h in hypotheses:
        delta = 0.0
        for channel, weight in calibration.channel_weights.items():
            w = max(0.0, float(weight))
            for key in aliases.get(channel, (channel,)):
                if key in h.evidence_terms:
                    delta += (w - 1.0) * float(h.evidence_terms[key])
        if delta:
            h.likelihood += delta
            h.free_energy -= delta
            h.evidence_terms["channel_weight_delta"] = (
                h.evidence_terms.get("channel_weight_delta", 0.0) + delta
            )


def _normalise_posterior(
    hypotheses: list[Hypothesis], calibration: CalibrationConfig | None = None
):
    """Convert free-energy values to a normalised posterior distribution.

    Uses calibrated softmax(-F / temperature) for numerical stability against
    very wide free-energy ranges. Temperature scaling is the standard
    post-hoc calibration method for overconfident classifiers.
    """
    if not hypotheses:
        return
    calibration = calibration or CalibrationConfig()
    base_temp = max(float(calibration.temperature), 1e-6)
    class_temps = calibration.class_temperatures or {}
    # subtract the min so the largest -F is 0 (stability)
    adjusted = []
    per_h_temp = []
    for h in hypotheses:
        bias = calibration.label_bias.get(h.label, 0.0)
        if h.orbital_class is not None:
            bias += calibration.label_bias.get(h.orbital_class, 0.0)
        adjusted.append(h.free_energy - bias)
        # Per-class temperature override -- look up by orbital_class first,
        # then by label, falling back to the global temperature.
        t = (
            class_temps.get(h.orbital_class)
            or class_temps.get(h.label)
            or class_temps.get(h.class_)
            or base_temp
        )
        per_h_temp.append(max(float(t), 1e-6))
    min_F = min(a for a in adjusted if math.isfinite(a))
    weights = []
    for a, t in zip(adjusted, per_h_temp):
        if not math.isfinite(a):
            weights.append(0.0)
        else:
            weights.append(math.exp(-((a - min_F) / t)))
    total = sum(weights)
    if total <= 0:
        for h in hypotheses:
            h.posterior = 1.0 / len(hypotheses)
        return
    for h, w in zip(hypotheses, weights):
        h.posterior = w / total


def _shannon_entropy(probs: list[float]) -> float:
    """H = -sum p_i log p_i in nats."""
    s = 0.0
    for p in probs:
        if p > 0:
            s -= p * math.log(p)
    return s


def audit_evidence(evidence: Evidence) -> EvidenceAudit:
    """Assess evidence completeness and contradictions before inference.

    This is intentionally conservative. It does not reject sparse evidence,
    but it records when the posterior should be treated as low-confidence.
    """
    channels = []
    warnings = []
    contradictions = []
    for name, value in (
        ("sky_position", evidence.ra_deg is not None and evidence.dec_deg is not None),
        ("rate", evidence.rate_arcsec_hr is not None),
        ("magnitude", evidence.apparent_mag is not None),
        ("morphology", evidence.morphology_label is not None),
        ("tracklet_arc", evidence.n_detections > 1 or evidence.arc_days > 0),
        ("orbit_fit", evidence.orbit_state is not None or evidence.rms_arcsec is not None),
        ("known_object_xmatch", evidence.skybot_match_names is not None),
        ("color", bool(evidence.band_magnitudes)),
    ):
        if value:
            channels.append(name)
    if evidence.n_detections <= 0:
        contradictions.append("n_detections must be positive")
    if evidence.arc_days < 0:
        contradictions.append("arc_days cannot be negative")
    if evidence.rate_arcsec_hr is not None and evidence.rate_arcsec_hr < 0:
        contradictions.append("rate_arcsec_hr cannot be negative")
    if evidence.morphology_confidence is not None and not (
        0.0 <= evidence.morphology_confidence <= 1.0
    ):
        contradictions.append("morphology_confidence outside [0,1]")
    if (
        evidence.morphology_label == "COSMIC_RAY"
        and (evidence.morphology_confidence is None or evidence.morphology_confidence >= 0.85)
        and evidence.n_detections >= 3
    ):
        contradictions.append("cosmic-ray morphology conflicts with repeated detections")
    if (
        evidence.rate_arcsec_hr is not None
        and evidence.rate_arcsec_hr > 1000
        and evidence.arc_days > 3
    ):
        contradictions.append("satellite-scale rate conflicts with multi-day coherent arc")
    if (
        evidence.morphology_label == "COSMIC_RAY"
        and evidence.morphology_confidence is not None
        and evidence.morphology_confidence < 0.85
        and evidence.rate_arcsec_hr is not None
        and evidence.n_detections >= 2
    ):
        warnings.append("weak cosmic-ray morphology with tracklet evidence needs manual review")
    if (
        evidence.n_detections <= 2
        and evidence.arc_days <= 0.1
        and evidence.rate_arcsec_hr is not None
        and evidence.apparent_mag is not None
        and evidence.morphology_label == "POINT"
    ):
        warnings.append(
            "short-arc moving-object candidate should not be discarded without follow-up"
        )
    if evidence.rms_arcsec is not None and evidence.rms_arcsec > 30 and evidence.n_detections >= 4:
        warnings.append("large orbit-fit RMS weakens moving-object claims")
    if len(channels) <= 2:
        warnings.append("sparse evidence: posterior should be treated as provisional")
    completeness = len(channels) / 8.0
    return EvidenceAudit(
        n_channels=len(channels),
        channels=channels,
        completeness=completeness,
        warnings=warnings,
        contradictions=contradictions,
        fail_closed=bool(contradictions),
    )


def posterior_predictive_check(best: Hypothesis, evidence: Evidence) -> PosteriorPredictiveCheck:
    """Check whether the winning hypothesis predicts the observed evidence."""
    if best is None:
        return PosteriorPredictiveCheck(warnings=["no winning hypothesis"], score=0.0)
    checked = []
    residuals = {}
    warnings = []
    score = 1.0

    if evidence.rate_arcsec_hr is not None and best.predicted_motion_arcsec_hr is not None:
        checked.append("rate")
        lo = hi = None
        if best.orbital_class in EXPECTED_RATES:
            lo, hi = EXPECTED_RATES[best.orbital_class]
        if lo is not None:
            if evidence.rate_arcsec_hr < lo:
                miss = lo - evidence.rate_arcsec_hr
            elif evidence.rate_arcsec_hr > hi:
                miss = evidence.rate_arcsec_hr - hi
            else:
                miss = 0.0
            residuals["rate_miss_arcsec_hr"] = miss
            if miss > max(1.0, 0.25 * (hi - lo)):
                warnings.append("observed rate is outside winning hypothesis range")
                score *= 0.5

    if evidence.morphology_label is not None and best.morphology_class is not None:
        checked.append("morphology")
        ok = evidence.morphology_label == best.morphology_class or (
            best.morphology_class == "POINT" and evidence.morphology_label == "BLEND"
        )
        residuals["morphology_match"] = bool(ok)
        if not ok:
            warnings.append("morphology does not match winning hypothesis")
            score *= 0.5

    if evidence.rms_arcsec is not None and math.isfinite(evidence.rms_arcsec):
        checked.append("rms")
        residuals["rms_arcsec"] = evidence.rms_arcsec
        if best.class_ == "moving_object" and evidence.rms_arcsec > 10:
            warnings.append("moving-object winner has high orbit-fit RMS")
            score *= 0.5

    return PosteriorPredictiveCheck(
        checked=checked,
        residuals=residuals,
        warnings=warnings,
        score=max(0.0, min(1.0, score)),
    )


def _jsonable(x):
    if isinstance(x, np.ndarray):
        return [_jsonable(v) for v in x.tolist()]
    if isinstance(x, np.generic):
        return x.item()
    if isinstance(x, float):
        if not math.isfinite(x):
            return None
        return x
    if isinstance(x, (str, int, bool)) or x is None:
        return x
    if isinstance(x, (list, tuple)):
        return [_jsonable(v) for v in x]
    if isinstance(x, dict):
        return {str(k): _jsonable(v) for k, v in x.items()}
    if hasattr(x, "__dataclass_fields__"):
        return _jsonable(asdict(x))
    return str(x)


def inference_certificate(evidence: Evidence, result: InferenceResult) -> dict:
    """Tamper-evident summary of an inference run."""
    payload = {
        "schema": "ariadne.discovery.inference_certificate.v1",
        "evidence": asdict(evidence),
        "calibration": asdict(result.calibration),
        "audit": asdict(result.evidence_audit) if result.evidence_audit else None,
        "posterior_check": asdict(result.posterior_check) if result.posterior_check else None,
        "top_hypotheses": [
            {
                "label": h.label,
                "class": h.class_,
                "orbital_class": h.orbital_class,
                "posterior": h.posterior,
                "free_energy": h.free_energy,
                "evidence_terms": h.evidence_terms,
            }
            for h in result.hypotheses[:5]
        ],
        "entropy": result.entropy,
        "recommended_followup": result.recommended_followup,
    }
    canonical = json.dumps(_jsonable(payload), sort_keys=True, separators=(",", ":"))
    return {
        "payload_hash": hashlib.sha256(canonical.encode("utf-8")).hexdigest(),
        "schema": payload["schema"],
        "top_label": result.best.label if result.best else None,
        "n_hypotheses": len(result.hypotheses),
    }


def validate_inference_certificate(evidence: Evidence, result: InferenceResult) -> bool:
    """Return True iff the stored certificate matches current evidence/result."""
    if not result.certificate:
        return False
    return result.certificate.get("payload_hash") == inference_certificate(evidence, result).get(
        "payload_hash"
    )


@dataclass
class ReliabilityReport:
    """Calibration quality over a labelled validation set."""

    n: int
    accuracy: float
    nll: float
    brier: float
    ece: float
    bins: list


def _matches_truth(h: Hypothesis, truth_label: str) -> bool:
    return truth_label in (h.label, h.orbital_class, h.class_)


def reliability_report(
    cases: list[tuple[Evidence, str]],
    *,
    calibration: CalibrationConfig | None = None,
    n_bins: int = 10,
) -> ReliabilityReport:
    """Expected calibration error and scoring rules for labelled cases.

    `truth_label` may be a full hypothesis label, an orbital_class, or a broad
    class_ such as "artefact". This keeps the report useful for small corpora
    where only coarse labels are available.
    """
    if not cases:
        return ReliabilityReport(0, 0.0, float("inf"), float("inf"), float("inf"), [])
    rows = []
    nll = 0.0
    brier = 0.0
    correct = 0
    for ev, truth in cases:
        r = infer(ev, calibration=calibration)
        p_true = 0.0
        for h in r.hypotheses:
            if _matches_truth(h, truth):
                p_true += h.posterior
        p_true = max(min(p_true, 1.0), 1e-12)
        is_correct = bool(r.best and _matches_truth(r.best, truth))
        correct += int(is_correct)
        conf = r.best.posterior if r.best else 0.0
        rows.append((conf, is_correct))
        nll -= math.log(p_true)
        brier += (1.0 - p_true) ** 2

    bins = []
    ece = 0.0
    for b in range(n_bins):
        lo, hi = b / n_bins, (b + 1) / n_bins
        bucket = [(c, ok) for c, ok in rows if lo <= c < hi or (b == n_bins - 1 and c == 1.0)]
        if not bucket:
            continue
        avg_conf = statistics.mean(c for c, _ in bucket)
        acc = statistics.mean(1.0 if ok else 0.0 for _, ok in bucket)
        weight = len(bucket) / len(rows)
        ece += weight * abs(avg_conf - acc)
        bins.append(
            {"lo": lo, "hi": hi, "n": len(bucket), "avg_confidence": avg_conf, "accuracy": acc}
        )
    n = len(cases)
    return ReliabilityReport(
        n=n, accuracy=correct / n, nll=nll / n, brier=brier / n, ece=ece, bins=bins
    )


def fit_temperature(
    cases: list[tuple[Evidence, str]],
    grid: tuple[float, ...] = (0.5, 0.75, 1.0, 1.25, 1.5, 2.0, 3.0, 4.0),
) -> tuple[CalibrationConfig, ReliabilityReport]:
    """Choose a global temperature by minimum validation NLL."""
    best_cfg = CalibrationConfig()
    best_rep = reliability_report(cases, calibration=best_cfg)
    for t in grid:
        cfg = CalibrationConfig(temperature=t, version=f"temperature-grid-{t:g}")
        rep = reliability_report(cases, calibration=cfg)
        if rep.nll < best_rep.nll:
            best_cfg, best_rep = cfg, rep
    return best_cfg, best_rep


def fit_per_class_temperatures(
    cases: list[tuple[Evidence, str]],
    *,
    grid: tuple[float, ...] = (0.3, 0.5, 0.75, 1.0, 1.5, 2.0, 3.0, 5.0, 10.0),
    global_temperature: float | None = None,
) -> tuple[CalibrationConfig, ReliabilityReport]:
    """Fit a per-class temperature on labelled cases.

    For each truth label appearing in `cases`, sweep the grid and pick the
    T that minimises NLL. The result is a CalibrationConfig with
    class_temperatures populated.

    If global_temperature is provided, every class without enough samples
    falls back to that; otherwise the class's own global-grid best.
    """
    if not cases:
        return CalibrationConfig(), ReliabilityReport(
            0, 0.0, float("inf"), float("inf"), float("inf"), []
        )
    # Start from the best global T as the seed
    if global_temperature is None:
        seed_cfg, seed_rep = fit_temperature(cases, grid=grid)
        global_T = seed_cfg.temperature
    else:
        global_T = float(global_temperature)
        seed_rep = reliability_report(cases, calibration=CalibrationConfig(temperature=global_T))

    # Group cases by truth label
    from collections import defaultdict

    by_label: dict[str, list] = defaultdict(list)
    for ev, truth in cases:
        by_label[truth].append((ev, truth))

    class_temps: dict[str, float] = {}
    for label, label_cases in by_label.items():
        if len(label_cases) < 2:
            # not enough data to fit a class-specific T; inherit global
            continue
        best_T = global_T
        best_nll = float("inf")
        for t in grid:
            cfg = CalibrationConfig(temperature=global_T, class_temperatures={label: t})
            rep = reliability_report(label_cases, calibration=cfg)
            if rep.nll < best_nll:
                best_nll = rep.nll
                best_T = t
        # Only persist if it's clearly different from the global default
        if abs(best_T - global_T) > 0.05:
            class_temps[label] = best_T

    final_cfg = CalibrationConfig(
        temperature=global_T,
        class_temperatures=class_temps,
        version=f"per-class-fitted-{global_T:g}",
    )
    final_rep = reliability_report(cases, calibration=final_cfg)
    # Only adopt if NLL actually improved over the global-only fit
    if final_rep.nll > seed_rep.nll:
        return CalibrationConfig(
            temperature=global_T, version=f"global-only-{global_T:g}"
        ), seed_rep
    return final_cfg, final_rep


def save_calibration(cfg: CalibrationConfig, path) -> None:
    """Persist a CalibrationConfig to a JSON file."""
    from pathlib import Path

    payload = {
        "schema": "ariadne.discovery.inference.calibration.v1",
        "temperature": cfg.temperature,
        "label_bias": dict(cfg.label_bias),
        "channel_weights": dict(cfg.channel_weights),
        "class_temperatures": dict(cfg.class_temperatures),
        "version": cfg.version,
    }
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(json.dumps(payload, indent=2), encoding="utf-8")


def load_calibration(path) -> CalibrationConfig:
    """Read back a CalibrationConfig saved by save_calibration."""
    from pathlib import Path

    raw = json.loads(Path(path).read_text(encoding="utf-8"))
    return CalibrationConfig(
        temperature=float(raw.get("temperature", 1.0)),
        label_bias=dict(raw.get("label_bias", {})),
        channel_weights=dict(raw.get("channel_weights", {})),
        class_temperatures={k: float(v) for k, v in raw.get("class_temperatures", {}).items()},
        version=raw.get("version", "loaded"),
    )


def _build_narrative(result: InferenceResult, evidence: Evidence) -> str:
    """Produce a human-readable summary of the inference."""
    if not result.hypotheses:
        return "No hypotheses generated."
    best = result.best
    second = result.hypotheses[1] if len(result.hypotheses) > 1 else None

    lines = []
    if best.posterior > 0.6:
        lines.append(f"HIGH-CONFIDENCE: {best.label} ({best.posterior * 100:.0f}%).")
    elif best.posterior > 0.35:
        lines.append(
            f"LEADING HYPOTHESIS: {best.label} ({best.posterior * 100:.0f}%); "
            f"runner-up {second.label if second else 'n/a'} "
            f"({second.posterior * 100:.0f}% if second else 0)."
        )
    else:
        lines.append(
            f"AMBIGUOUS: top guess {best.label} only "
            f"{best.posterior * 100:.0f}%; evidence is sparse "
            f"(entropy {result.entropy:.2f} nats)."
        )

    # Evidence summary
    parts = []
    if evidence.rate_arcsec_hr is not None:
        parts.append(f'rate {evidence.rate_arcsec_hr:.2f}"/hr')
    if evidence.apparent_mag is not None:
        parts.append(f"mag {evidence.apparent_mag:.1f}")
    if evidence.morphology_label is not None:
        parts.append(f"morph {evidence.morphology_label}")
    if evidence.skybot_match_names is not None and not evidence.skybot_match_names:
        parts.append("no SkyBoT match")
    if evidence.n_detections > 1:
        parts.append(f"{evidence.n_detections} detections over {evidence.arc_days:.1f}d")
    if parts:
        lines.append(f"  Evidence: {', '.join(parts)}.")
    if result.evidence_audit:
        audit = result.evidence_audit
        lines.append(
            f"  Evidence audit: {audit.n_channels}/8 channels, "
            f"completeness {audit.completeness:.2f}."
        )
        for w in audit.warnings[:2]:
            lines.append(f"    warning: {w}")
        for c in audit.contradictions[:2]:
            lines.append(f"    contradiction: {c}")
    if result.posterior_check and result.posterior_check.warnings:
        lines.append("  Posterior check: " + "; ".join(result.posterior_check.warnings[:2]))

    # Top-3 ranking
    lines.append("  Top hypotheses:")
    for h in result.hypotheses[:3]:
        lines.append(f"    {h.posterior * 100:>5.1f}%  {h.label:<28s}  ({h.explanation[:64]})")
        if h.evidence_terms:
            strongest = sorted(h.evidence_terms.items(), key=lambda kv: abs(kv[1]), reverse=True)[
                :2
            ]
            desc = ", ".join(f"{k}={v:+.2f}" for k, v in strongest)
            lines.append(f"           drivers: {desc}")

    if result.recommended_followup:
        f = result.recommended_followup
        lines.append(f"  Recommended follow-up: {f.get('action', '?')} -> {f.get('reason', '')}")

    if result.memory_matches:
        n = len(result.memory_matches)
        lines.append(f"  Episodic recall: {n} similar past candidate(s) in store.")

    return "\n".join(lines)


def _episodic_recall(
    evidence: Evidence,
    store: CandidateStore,
    max_matches: int = 5,
    sky_tol_arcmin: float = 60.0,
    rate_tol_arcsec_hr: float = 1.0,
) -> list[Candidate]:
    """Search the candidate store for prior detections similar to this evidence."""
    if store is None or evidence.ra_deg is None or evidence.dec_deg is None:
        return []
    matches = []
    for c in store:
        dra = (c.ra - evidence.ra_deg) * math.cos(math.radians(evidence.dec_deg))
        ddec = c.dec - evidence.dec_deg
        sep_arcmin = math.hypot(dra, ddec) * 60.0
        if sep_arcmin > sky_tol_arcmin:
            continue
        if evidence.rate_arcsec_hr is not None:
            if abs(c.rate_arcsec_hr - evidence.rate_arcsec_hr) > rate_tol_arcsec_hr:
                continue
        matches.append((sep_arcmin, c))
    matches.sort(key=lambda x: x[0])
    return [m[1] for m in matches[:max_matches]]


def _pareto_front(hypotheses: list[Hypothesis]) -> list[Hypothesis]:
    """Return the Pareto-optimal hypotheses on (posterior, novelty) axes.

    novelty = -log(prior) (rare populations score higher).
    A hypothesis is dominated if there exists another with higher posterior
    AND higher novelty.
    """
    items = []
    for h in hypotheses:
        novelty = -h.prior
        items.append((h, h.posterior, novelty))
    front = []
    for h, p, n in items:
        dominated = False
        for h2, p2, n2 in items:
            if h is h2:
                continue
            if p2 >= p and n2 >= n and (p2 > p or n2 > n):
                dominated = True
                break
        if not dominated:
            front.append(h)
    front.sort(key=lambda h: h.posterior, reverse=True)
    return front


def _recommend_followup(result: InferenceResult, evidence: Evidence) -> dict:
    """Recommend the NEXT observation that would maximally reduce uncertainty.

    Heuristics:
      - High entropy (>1.5 nats): take a 2nd-night confirmation observation.
      - Top hypothesis is high-novelty (rare class): submit to MPC + alert.
      - Top hypothesis is artefact: tag as bogus, no follow-up.
      - Mid-entropy: take a multi-band photometric observation to constrain
        the color-based classification.
    """
    if not result.hypotheses:
        return {"action": "no_action", "reason": "no hypotheses"}
    best = result.best
    entropy = result.entropy

    if (
        evidence.morphology_label == "COSMIC_RAY"
        and evidence.morphology_confidence is not None
        and evidence.morphology_confidence < 0.85
        and evidence.rate_arcsec_hr is not None
        and evidence.n_detections >= 2
    ):
        has_orbit_context = (
            evidence.orbit_state is not None
            or {"a_au", "e"}.issubset(evidence.sky_context)
            or evidence.skybot_match_names is not None
        )
        if not has_orbit_context:
            return {
                "action": "manual_review",
                "reason": (
                    "weak cosmic-ray morphology conflicts with tracklet "
                    "evidence and lacks orbit/xmatch context"
                ),
                "expected_info_gain_nats": entropy,
                "source": "adversarial_audit",
            }
        return {
            "action": "observe_second_night",
            "reason": (
                "weak cosmic-ray morphology conflicts with moving-tracklet "
                "evidence; preserve class prediction and verify"
            ),
            "expected_info_gain_nats": entropy,
            "source": "adversarial_audit",
        }

    if best.class_ == "artefact":
        if (
            evidence.n_detections >= 2
            and evidence.arc_days <= 0.1
            and evidence.rate_arcsec_hr is not None
            and evidence.morphology_label == "POINT"
        ):
            return {
                "action": "observe_second_night",
                "reason": (
                    "short-arc moving candidate has insufficient evidence "
                    "for discard; obtain another night"
                ),
                "expected_info_gain_nats": max(entropy, 0.5),
                "observation_target": {
                    "ra_deg": evidence.ra_deg,
                    "dec_deg": evidence.dec_deg,
                    "expected_rate_arcsec_hr": best.predicted_motion_arcsec_hr,
                },
            }
        return {
            "action": "discard",
            "reason": f"top hypothesis is {best.label} (artefact); not worth follow-up",
            "expected_info_gain_nats": 0.0,
        }

    if best.orbital_class == "MBA":
        trojan_alt = next(
            (h for h in result.hypotheses[:5] if h.orbital_class in {"JTROJAN", "HILDA"}),
            None,
        )
        if (
            trojan_alt is not None
            and evidence.rate_arcsec_hr is not None
            and 3.0 <= evidence.rate_arcsec_hr <= 7.0
            and evidence.band_magnitudes
        ):
            return {
                "action": "observe_multiband",
                "reason": ("MBA/Trojan posterior is not decisive for a red, Jovian-rate candidate"),
                "expected_info_gain_nats": max(entropy, 0.5),
                "observation_target": {
                    "ra_deg": evidence.ra_deg,
                    "dec_deg": evidence.dec_deg,
                    "expected_rate_arcsec_hr": best.predicted_motion_arcsec_hr,
                },
            }

    if entropy > 1.5:
        return {
            "action": "observe_second_night",
            "reason": (
                f"posterior is ambiguous (entropy {entropy:.2f} nats); "
                f"a second-night detection would constrain the orbit"
            ),
            "expected_info_gain_nats": min(entropy, 2.0),
            "observation_target": {
                "ra_deg": evidence.ra_deg,
                "dec_deg": evidence.dec_deg,
                "expected_rate_arcsec_hr": best.predicted_motion_arcsec_hr,
                "expected_displacement_arcmin_per_day": (
                    best.predicted_motion_arcsec_hr * 24.0 / 60.0
                    if best.predicted_motion_arcsec_hr
                    else None
                ),
            },
        }

    if len(result.hypotheses) > 1:
        margin = result.hypotheses[0].posterior - result.hypotheses[1].posterior
        if best.class_ == "moving_object" and (margin < 0.20 or best.posterior < 0.60):
            return {
                "action": "observe_second_night",
                "reason": (
                    f"posterior margin is narrow ({margin:.2f}); "
                    "another night is needed before treating the class as settled"
                ),
                "expected_info_gain_nats": max(entropy * 0.5, 0.2),
                "observation_target": {
                    "ra_deg": evidence.ra_deg,
                    "dec_deg": evidence.dec_deg,
                    "expected_rate_arcsec_hr": best.predicted_motion_arcsec_hr,
                },
            }

    if best.class_ == "moving_object" and best.orbital_class in (
        "SEDNOID",
        "DETACHED",
        "RESONANT_KBO",
        "COMET_HYPERBOLIC",
    ):
        return {
            "action": "alert_and_submit_mpc",
            "reason": f"high-novelty {best.orbital_class} candidate at {best.posterior * 100:.0f}%",
            "expected_info_gain_nats": entropy,
        }

    if 0.5 < entropy <= 1.5 and not evidence.band_magnitudes:
        return {
            "action": "observe_multiband",
            "reason": (
                "posterior is moderately confident; multi-band photometry "
                "would constrain compositional class"
            ),
            "expected_info_gain_nats": entropy * 0.5,
            "observation_target": {
                "ra_deg": evidence.ra_deg,
                "dec_deg": evidence.dec_deg,
                "bands_needed": ["g", "r", "i", "z"],
            },
        }

    return {
        "action": "monitor",
        "reason": f"sufficient confidence ({best.posterior * 100:.0f}%) on {best.label}",
        "expected_info_gain_nats": entropy * 0.1,
    }


