@dataclass
class Evidence:
    """Container for ANY combination of available evidence on one (or several)
    putative objects. Every field is optional; the engine works with whatever
    is provided.

    Fields:
      mjd:                   epoch of the most recent observation.
      ra_deg, dec_deg:       sky position (most recent or single observation).
      rate_arcsec_hr:        on-sky angular rate, if known.
      apparent_mag:          most recent observed magnitude.
      band:                  photometric band character (g, r, i, z, V).
      band_magnitudes:       full multi-band dict (e.g. {"g":21.5, "r":20.9}).
      morphology_label:      a MorphologyClass label, if image-pipeline ran.
      morphology_confidence: associated confidence (0..1).
      n_detections:          count of independent detections.
      arc_days:              span of the arc, days.
      rms_arcsec:            orbit-fit RMS if a fit was attempted.
      orbit_state:           fitted [x,y,z,vx,vy,vz] heliocentric km/km/s.
      skybot_match_names:    SkyBoT cross-match results, empty list if checked
                              and clean, None if not checked.
      sky_context:           extra dict for any sidereal context (galactic
                              latitude, field crowding, etc.).
    """

    mjd: float | None = None
    ra_deg: float | None = None
    dec_deg: float | None = None
    rate_arcsec_hr: float | None = None
    apparent_mag: float | None = None
    band: str | None = None
    band_magnitudes: dict = field(default_factory=dict)
    morphology_label: str | None = None
    morphology_confidence: float | None = None
    n_detections: int = 1
    arc_days: float = 0.0
    rms_arcsec: float | None = None
    orbit_state: list | None = None
    skybot_match_names: list | None = None
    sky_context: dict = field(default_factory=dict)


