# ============================================================================
# Likelihood functions (how well each hypothesis fits evidence)
# ============================================================================


def _gaussian_log_pdf(x: float, mu: float, sigma: float) -> float:
    """Log of N(mu, sigma) density at x. Clipped to prevent -inf."""
    if sigma <= 0:
        return -1e6
    z = (x - mu) / sigma
    return -0.5 * z * z - math.log(sigma * math.sqrt(2 * math.pi))


def _uniform_log_pdf_in_range(
    x: float, lo: float, hi: float, soft_outside_pct: float = 0.2
) -> float:
    """Log-uniform inside [lo, hi], with soft exponential decay outside.

    soft_outside_pct controls how quickly the density falls when x leaves the
    range: at lo*(1-soft), the log-pdf drops by 1 below the in-range value.
    """
    if lo <= x <= hi:
        return -math.log(max(hi - lo, 1e-6))
    width = hi - lo
    if width <= 0:
        return -1e6
    if x < lo:
        d = (lo - x) / max(width * soft_outside_pct, 1e-6)
    else:
        d = (x - hi) / max(width * soft_outside_pct, 1e-6)
    return -math.log(width) - d * d


def _rate_log_likelihood(observed_rate: float, class_name: str) -> float:
    """How well the observed on-sky rate fits the expected rate for this class.

    A rate inside the physical window is evidence in favour of the class, not
    a penalty for having a wide valid range. Outside the window, an aggressive
    soft penalty down-weights the class even against a large prior.
    """
    if class_name not in EXPECTED_RATES:
        return -2.0
    lo, hi = EXPECTED_RATES[class_name]
    if lo <= observed_rate <= hi:
        return 0.0
    if observed_rate < lo:
        d = (lo - observed_rate) / max(lo * 0.25, 0.1)
    else:
        d = (observed_rate - hi) / max(hi * 0.25, 0.1)
    return -d * d


def _magnitude_log_likelihood(
    observed_mag: float, class_name: str, expected_distance_au: float
) -> float:
    """Distance + size prior under H(mag, distance, albedo).

    For a class with typical distance D, expected size range S, albedo A:
      apparent_mag ~ H + 5 log10(D * delta) where delta ~= D (opposition)
      H = absolute mag = -5 log10(D_sun) - 5 log10(D_obs) + apparent
    We score the consistency: large objects + close distances are bright;
    if the observed mag is way out of the expected range for the class,
    likelihood drops.
    """
    if not math.isfinite(observed_mag) or observed_mag < 0 or observed_mag > 35:
        return -2.0
    # Quick magnitude-distance check: NEOs at V<15, MBAs at V<19, TNOs at V>20
    if class_name in ("ATIRA", "ATEN", "APOLLO", "AMOR"):
        return _uniform_log_pdf_in_range(observed_mag, 8.0, 22.0, soft_outside_pct=0.3)
    if class_name in ("MBA", "IMB", "OMB", "MARS_CROSSER", "HUNGARIA"):
        return _uniform_log_pdf_in_range(observed_mag, 5.0, 22.5, soft_outside_pct=0.3)
    if class_name in ("HILDA", "THULE", "JTROJAN"):
        return _uniform_log_pdf_in_range(observed_mag, 8.0, 22.5, soft_outside_pct=0.3)
    if class_name == "CENTAUR":
        return _uniform_log_pdf_in_range(observed_mag, 12.0, 23.0, soft_outside_pct=0.3)
    if class_name in (
        "CLASSICAL_KBO",
        "HOT_CLASSICAL",
        "RESONANT_KBO",
        "SCATTERED_KBO",
        "DETACHED",
        "SEDNOID",
    ):
        return _uniform_log_pdf_in_range(observed_mag, 14.0, 25.5, soft_outside_pct=0.3)
    if class_name == "COMET_HYPERBOLIC":
        return _uniform_log_pdf_in_range(observed_mag, 14.0, 23.0, soft_outside_pct=0.4)
    return -1.0


def _morphology_log_likelihood(
    observed_label: str, observed_conf: float, expected_label: str
) -> float:
    """How well the morphology matches the hypothesis's expected morphology."""
    if observed_label == expected_label:
        return math.log(max(observed_conf, 0.05))
    # any moving-object hypothesis EXPECTS POINT; if observed is BLEND, that's
    # a possible match (one of the components might be the asteroid).
    if expected_label == "POINT" and observed_label == "BLEND":
        return math.log(0.30)
    # Streaks indicate fast NEOs or satellites
    if observed_label == "STREAK" and expected_label == "POINT":
        return math.log(0.10)
    # A weak cosmic-ray flag is common in crowded/noisy real CCDs. Treat it as
    # mild negative evidence against a moving object, not as a decisive veto;
    # the audit layer handles high-confidence contradictions separately.
    if observed_label == "COSMIC_RAY" and expected_label == "POINT" and observed_conf < 0.85:
        return math.log(0.45)
    return math.log(max(0.05, 1.0 - observed_conf))


def _coherent_short_arc_motion(evidence: Evidence) -> bool:
    """True when sparse evidence still has enough motion to resist artefact priors."""
    return (
        evidence.rate_arcsec_hr is not None
        and evidence.rate_arcsec_hr > 0.2
        and evidence.n_detections >= 2
        and evidence.arc_days > 0.0
        and evidence.morphology_label in (None, "POINT", "BLEND")
    )


def _orbit_state_log_likelihood(orbit_state: list, class_name: str) -> float:
    """If we have a fitted orbit, the orbital class derived from it should match."""
    if orbit_state is None or len(orbit_state) != 6:
        return 0.0
    try:
        r = np.asarray(orbit_state[:3])
        v = np.asarray(orbit_state[3:])
        tax = taxonomy.classify_state(r, v)
        if tax.label == class_name:
            return math.log(max(tax.confidence, 0.05))
        return math.log(0.10)
    except Exception:
        return -1.0


def _color_log_likelihood(band_magnitudes: dict, class_name: str) -> float:
    """Broad color evidence for sparse alerts.

    This is deliberately coarse: without calibrated photometry we only use it
    to separate red outer-solar-system candidates from neutral inner-belt ones.
    """
    if not band_magnitudes:
        return 0.0
    g = band_magnitudes.get("g")
    r = band_magnitudes.get("r")
    i = band_magnitudes.get("i")
    if g is None or r is None:
        return 0.0
    try:
        gr = float(g) - float(r)
        ri = float(r) - float(i) if i is not None else 0.0
    except Exception:
        return 0.0
    red = gr >= 0.45 and ri >= 0.10
    very_red = gr >= 0.60 and ri >= 0.20
    if class_name in ("JTROJAN", "HILDA"):
        return math.log(5.0 if very_red else 2.0 if red else 0.7)
    if class_name in (
        "CLASSICAL_KBO",
        "HOT_CLASSICAL",
        "RESONANT_KBO",
        "SCATTERED_KBO",
        "DETACHED",
        "SEDNOID",
        "CENTAUR",
    ):
        return math.log(3.0 if red else 0.8)
    if class_name in ("MBA", "IMB", "OMB") and very_red:
        return math.log(0.45)
    return 0.0


