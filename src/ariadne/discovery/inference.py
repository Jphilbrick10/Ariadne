"""Cognitive Inference Engine -- multi-signal Bayesian fusion + extreme guessing.

When evidence is sparse (one detection, one trail, a magnitude bump in an
empty patch of sky), traditional discovery pipelines say "insufficient data."
This engine instead computes a posterior distribution over competing
HYPOTHESES, ranked by the cumulative evidence available, and returns the
most-educated guess WITH its uncertainty.

Architecture:

  1. Evidence ingestion: take any combination of Source(s), tracklet(s),
     candidate(s), multi-band photometry, sky-context.
  2. Hypothesis generation: enumerate physically-plausible explanations.
     The hypothesis SPACE is generated by walking taxonomy classes (NEO/MBA/
     Trojan/Centaur/TNO/Comet/Artefact/etc.) and the morphology classes
     (POINT/EXTENDED/STREAK/BLEND/COSMIC_RAY).
  3. Free-energy scoring: each hypothesis pays a "complexity cost" (prior
     under the dynamical population model) + an "accuracy cost" (how well it
     explains the observations). Lower free energy = better.
  4. Posterior: softmax over free-energies -> probability distribution.
  5. Episodic recall: query the candidate store for past detections with
     similar (position, rate, color). Memory acts as a prior.
  6. Active inference: recommend the NEXT observation that would maximally
     reduce uncertainty over the top hypotheses.
  7. Pareto ranking: when multiple objectives matter (probability * novelty
     * follow-up cost), the Pareto front gives the best trade-offs.
  8. Predictive scheduler: learns from past confirmation/rejection outcomes
     which observation strategy maximises information gain for each
     hypothesis class. Adaptive over time.

The engine COMPOSES (not replaces) every classifier/scorer/predictor module:
morphology, colors, taxonomy, realbogus, scoring, followup. It is the
brain that ties them.

Output: an InferenceResult containing ranked hypotheses, predicted next
positions, recommended follow-up, and human-readable explanations.

References:
  - Friston 2010 (free-energy principle); Friston & FitzGerald 2017 (active
    inference).
  - Granvik & Brown 2018 (NEO population model); Petit et al. 2011 (TNO
    population priors); Bowell 1989 (asteroid taxonomic priors).
  - Holman et al. 2018 (HelioLinC sparse-data linker).
"""

