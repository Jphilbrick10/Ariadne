def infer(
    evidence: Evidence,
    store: CandidateStore | None = None,
    *,
    class_priors: dict = None,
    artefact_priors: dict = None,
    include_artefact_hypotheses: bool = True,
    scheduler=None,
    calibration: CalibrationConfig | None = None,
    fail_closed_on_contradiction: bool = False,
) -> InferenceResult:
    """One inference cycle on a piece of evidence. Returns the ranked posterior.

    Args:
      evidence:                Evidence object (any subset of fields).
      store:                   optional CandidateStore for episodic recall.
      class_priors:            override the default class prior fractions.
      artefact_priors:         override the artefact prior fractions.
      include_artefact_hypotheses: if True, also generate spurious-detection
                               hypotheses (recommended -- the cleanest way to
                               flag obvious junk).
      scheduler:               optional PredictiveScheduler -- if provided, the
                               follow-up recommendation uses the learned
                               historical confirmation rates instead of the
                               cold-start heuristic.
      calibration:             posterior temperature and label-bias correction.
      fail_closed_on_contradiction: when True, contradictory evidence returns
                               no ranked winner and recommends manual review.

    Returns:
      InferenceResult with hypotheses (ranked best first), entropy,
      recommended follow-up action, episodic-memory matches, and a
      human-readable narrative.
    """
    if class_priors is None:
        class_priors = DEFAULT_CLASS_PRIORS
    if artefact_priors is None:
        artefact_priors = ARTEFACT_PRIORS
    if calibration is None:
        calibration = CalibrationConfig()

    audit = audit_evidence(evidence)
    if fail_closed_on_contradiction and audit.fail_closed:
        result = InferenceResult(
            hypotheses=[],
            best=None,
            entropy=float("inf"),
            evidence_audit=audit,
            posterior_check=PosteriorPredictiveCheck(
                warnings=["contradictory evidence rejected before scoring"], score=0.0
            ),
            calibration=calibration,
        )
        result.recommended_followup = {
            "action": "manual_review",
            "reason": "contradictory evidence failed closed",
            "expected_info_gain_nats": 0.0,
            "source": "evidence_audit",
        }
        result.narrative = _build_narrative(result, evidence)
        result.certificate = inference_certificate(evidence, result)
        return result

    hyps = _generate_moving_object_hypotheses(evidence, class_priors)
    if include_artefact_hypotheses:
        hyps.extend(_generate_artefact_hypotheses(evidence, artefact_priors))

    _apply_channel_weights(hyps, calibration)
    _normalise_posterior(hyps, calibration)
    hyps.sort(key=lambda h: h.posterior, reverse=True)

    entropy = _shannon_entropy([h.posterior for h in hyps])
    memory = _episodic_recall(evidence, store) if store is not None else []
    pareto = _pareto_front(hyps)

    result = InferenceResult(
        hypotheses=hyps,
        best=hyps[0] if hyps else None,
        entropy=entropy,
        memory_matches=memory,
        pareto_front=pareto,
        evidence_audit=audit,
        calibration=calibration,
    )
    result.posterior_check = posterior_predictive_check(result.best, evidence)
    # If a learned scheduler is available, defer to it; else use the heuristic.
    if scheduler is not None and result.best is not None:
        from . import predictive

        ev_class = predictive.classify_evidence(evidence)
        reco = scheduler.recommend(
            evidence_class=ev_class,
            hypothesis_posterior=result.best.posterior,
            alternatives=result.hypotheses[:5],
        )
        result.recommended_followup = {
            "action": reco.action,
            "reason": reco.rationale,
            "expected_info_gain_nats": reco.expected_info_gain_nats,
            "expected_confirmation_prob": reco.expected_confirmation_prob,
            "evidence_class": ev_class,
            "cost": reco.cost,
            "separates": reco.separates,
            "source": "learned_scheduler",
        }
    else:
        result.recommended_followup = _recommend_followup(result, evidence)
        result.recommended_followup["source"] = "heuristic"
    result.narrative = _build_narrative(result, evidence)
    result.certificate = inference_certificate(evidence, result)
    return result


def evidence_from_candidate(c: Candidate) -> Evidence:
    """Convenience: build an Evidence object from a stored Candidate."""
    arc = max(0.0, c.last_seen_mjd - c.first_seen_mjd)
    rms = c.rms_history[-1][1] if c.rms_history else None
    band_mags = c.meta.get("band_magnitudes", {}) if c.meta else {}
    return Evidence(
        mjd=c.last_seen_mjd,
        ra_deg=c.ra,
        dec_deg=c.dec,
        rate_arcsec_hr=c.rate_arcsec_hr,
        apparent_mag=c.meta.get("apparent_mag") if c.meta else None,
        band_magnitudes=band_mags,
        n_detections=c.n_runs,
        arc_days=arc,
        rms_arcsec=rms,
        orbit_state=c.orbit_state,
        skybot_match_names=c.skybot_names,
    )


def infer_for_store(
    store: CandidateStore, *, only_discovery: bool = True
) -> list[tuple[Candidate, InferenceResult]]:
    """Run the inference engine on every candidate in a store.

    Args:
      only_discovery: when True, only run on discovery_candidates() (no
                      skybot match); when False, run on every candidate.

    Returns:
      List of (candidate, result) pairs, sorted by result.best.posterior * (1 - artefact prob).
    """
    cands = store.discovery_candidates() if only_discovery else list(store)
    out = []
    for c in cands:
        evidence = evidence_from_candidate(c)
        result = infer(evidence, store=store)
        out.append((c, result))

    def _rank(item):
        cand, res = item
        best = res.best
        if best is None:
            return 0.0
        artefact_penalty = 0.0
        for h in res.hypotheses:
            if h.class_ == "artefact":
                artefact_penalty += h.posterior
        return best.posterior * (1.0 - 0.5 * artefact_penalty)

    out.sort(key=_rank, reverse=True)
    return out
