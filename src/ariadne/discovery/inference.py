@dataclass
class Hypothesis:
    """One explanation for the evidence, with priors + per-likelihood scores.

    Fields:
      label:             human-readable name (e.g. "TNO 50 AU", "NEO Apollo",
                          "satellite trail", "cosmic ray", "stellar variable").
      class_:            broad category: "moving_object", "transient", "artefact",
                          "stellar".
      orbital_class:     OrbitClass.* label if class_ == "moving_object".
      morphology_class:  MorphologyClass.* label expected.
      prior:             log p(H) -- prior probability under the population model.
      likelihood:        log p(E|H) -- how well evidence fits the hypothesis.
      free_energy:       F = -log p(H) - log p(E|H); LOWER is better.
      posterior:         p(H|E) -- normalised over the hypothesis space.
      predicted_motion_arcsec_hr:  rate this hypothesis EXPECTS.
      predicted_size_km:           rough size from magnitude + assumed albedo.
      predicted_distance_au:       rough heliocentric distance.
      explanation:       free-text 'why this hypothesis fits' for humans.
      evidence_terms:    per-channel log-likelihood/prior contributions. This
                          is the audit trail for why the hypothesis won.
    """

    label: str
    class_: str = "unknown"
    orbital_class: str | None = None
    morphology_class: str | None = None
    prior: float = 0.0  # log prior
    likelihood: float = 0.0  # log likelihood
    free_energy: float = float("inf")
    posterior: float = 0.0
    predicted_motion_arcsec_hr: float | None = None
    predicted_size_km: float | None = None
    predicted_distance_au: float | None = None
    explanation: str = ""
    evidence_terms: dict = field(default_factory=dict)


@dataclass
class CalibrationConfig:
    """Posterior calibration knobs.

    Temperature > 1 softens overconfident posteriors; < 1 sharpens them.
    label_bias is an optional additive logit correction by label or orbital
    class learned from a validation corpus. channel_weights optionally scales
    named evidence terms before posterior normalisation. Defaults are neutral.
    """

    temperature: float = 1.0
    label_bias: dict = field(default_factory=dict)
    channel_weights: dict = field(default_factory=dict)
    class_temperatures: dict = field(default_factory=dict)
    version: str = "manual-v1"


@dataclass
class EvidenceAudit:
    """Quality and consistency report for one Evidence object."""

    n_channels: int
    channels: list
    completeness: float
    warnings: list = field(default_factory=list)
    contradictions: list = field(default_factory=list)
    fail_closed: bool = False


@dataclass
class PosteriorPredictiveCheck:
    """How well the winning hypothesis predicts observed evidence."""

    checked: list = field(default_factory=list)
    residuals: dict = field(default_factory=dict)
    warnings: list = field(default_factory=list)
    score: float = 1.0


@dataclass
class InferenceResult:
    """Output of one inference cycle on a piece of Evidence.

    Fields:
      hypotheses:               list of Hypothesis ordered by free_energy
                                 (best first). posterior sums to 1.
      best:                     hypothesis with highest posterior.
      entropy:                  Shannon entropy of the posterior (high =
                                 ambiguous; low = confident).
      recommended_followup:     dict {action, observation_target, expected_info_gain}
      memory_matches:           list of Candidate from the store that resemble
                                 this evidence (episodic recall).
      pareto_front:             multi-objective optimal hypotheses (probability,
                                 novelty, low follow-up cost).
      narrative:                human-readable summary of the inference.
      evidence_audit:           completeness/contradiction check.
      posterior_check:          posterior-predictive check for the winner.
      certificate:              deterministic evidence + posterior hash.
      calibration:              calibration config actually used.
    """

    hypotheses: list
    best: Hypothesis | None = None
    entropy: float = float("inf")
    recommended_followup: dict = field(default_factory=dict)
    memory_matches: list = field(default_factory=list)
    pareto_front: list = field(default_factory=list)
    narrative: str = ""
    evidence_audit: EvidenceAudit | None = None
    posterior_check: PosteriorPredictiveCheck | None = None
    certificate: dict = field(default_factory=dict)
    calibration: CalibrationConfig = field(default_factory=CalibrationConfig)


